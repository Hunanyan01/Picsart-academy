package am.picsartacademy.lesson.Lesson06.oop2;

public class Varord {
    private String anun;
    private int tariq;

    public Varord(String anun, int tariq) {
        this.anun = anun;
        this.tariq = tariq;
    }

    public String getAnun() {
        return anun;
    }

    public int getTariq() {
        return tariq;
    }
}
